# use print() function to print out a string
print("Hello World")
# use \n to add a new line in the output without having to keep typing print
print("Hello World!\nHello World!\nHello World!")
# use "+" to concatenate strings
print("Hello" + "John")

# input() will get user input in console
# then print() will print the word "Hello" and user input
print("Hello " + input("What is your name?"))

# to get string length, use len() function
s = input("What is your name?")
print(len(s))
